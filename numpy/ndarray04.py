import numpy as np

arr=np.array([[1. ,2. ,3.],[4. ,5. ,6.]])
print(arr)
# [[1. 2. 3.]
#  [4. 5. 6.]]

print(arr + arr)
# [[ 2.  4.  6.]
#  [ 8. 10. 12.]]

print(arr - arr)
# [[0. 0. 0.]
#  [0. 0. 0.]]

print(arr * arr)
# [[ 1.  4.  9.]
#  [16. 25. 36.]]

print(arr / arr)
# [[1. 1. 1.]
#  [1. 1. 1.]]